# Operadores Aritméticos e booleanos
# Operações Matemáticas (Aritméticas)

"""
1 Soma: +
2 Subtração: -
3 Multiplicação: *
4 Divisão:  /
5 Divisão inteira: // (descarta a parte decimal)
6 Resto da divisão: %
7 Potência: **
"""
print('\n Operações Matemáticas (Aritméticas) \n')
numero1 = 10
numero2 = 20

print("1- ", numero1 + numero2)
print("2- ", numero1 - numero2)
print("3- ", numero1 * numero2)
print("4- ", numero1 / numero2)
print("5- ", numero1 // numero2)
print("6- ", 20%3)
print("7- ", 2 **3 )

# Operadores Booleanos (lógicos)
"""
Maior: >
menor: <
Maior igual: >=
Menor igual: <=
Igual: ==
Diferente: !=

"""
print('\n Operadores Booleanos (lógicos) \n')
idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(idade1 > idade2)        #false
print(idade1 <= idade2)       #true
print('Python' == 'python')   #false
print('banana' != 'abacaxi')  #true
print(altura1 >= altura2)     #true
print(altura2 > altura3)      #false